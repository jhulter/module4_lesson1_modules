def mood_response(mood):
    if mood == "happy":
        print("Hooray you're happy!")
    elif mood == "sad":
        print("Cheer up buttercup!")
    elif mood == "excited":
        print("Calm down there cowboy!")
    elif mood == "angry":
        print("Geez, who pissed in your cheerios?")
    elif mood == "indifferent":
        print("Fine, I don't care either...")
    elif mood == "confused":
        print("I can't help ya there, I'm always confused...")
    else:
        print("Sorry I don't have a clever response for that one...")
